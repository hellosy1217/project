package com.web.blueballoon.model;

public class ImageDTO {
	private int check;
	private int selected_num;
	private String org_img;
	private String str_img;

	public int getCheck() {
		return check;
	}

	public void setCheck(int check) {
		this.check = check;
	}

	public int getSelected_num() {
		return selected_num;
	}

	public void setSelected_num(int selected_num) {
		this.selected_num = selected_num;
	}

	public String getOrg_img() {
		return org_img;
	}

	public void setOrg_img(String org_img) {
		this.org_img = org_img;
	}

	public String getStr_img() {
		return str_img;
	}

	public void setStr_img(String str_img) {
		this.str_img = str_img;
	}

}