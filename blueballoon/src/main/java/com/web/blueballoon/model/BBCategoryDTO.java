package com.web.blueballoon.model;

public class BBCategoryDTO {
	private int cate_num;
	private String cate_city;
	private String cate_state;

	public int getCate_num() {
		return cate_num;
	}

	public void setCate_num(int cate_num) {
		this.cate_num = cate_num;
	}

	public String getCate_city() {
		return cate_city;
	}

	public void setCate_city(String cate_city) {
		this.cate_city = cate_city;
	}

	public String getCate_state() {
		return cate_state;
	}

	public void setCate_state(String cate_state) {
		this.cate_state = cate_state;
	}

}
